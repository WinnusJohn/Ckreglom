                        looking man wearing only a kaki short with bushy hair on his head pursuing me. In that
                        revelation, I ran the best I could to escape from him until I saw a fenced but well lighted
                        camp, which I ran into it and shut the gate. I sat down then after, panting while my pursuer put
                        his finger in his mouth, shaking his head because he couldn’t enter the gate, as at that point,
                        a voice called out at me “go and open that gate”, by the time I looked up I saw this same
                        terrible being chasing a multitude of people, in obedience to the un-seen voice, I ran and
                        opened the gate and people began to run inside the camp. In the process as many as ran into the
                        camp became free from the grips of this wicked fellow. Then the Spirit of God told me that the
                        pursuer that I saw was Satan himself. And that a time will come that he will use me to begin a
                        place where people will be rescued from the menace of Satan. In 1990, I answered the call into
                        the ministry full time. I got trained in the Christ Apostolic Church Theological Seminary,
                        Ile-Ife, Osun State, and began ministering in a C.A.C. assembly. I almost never remembered my
                        earlier revelation, not until year 2000 that the Lord told me it was time to begin the
                        assignment he earlier told me about. Being a full blooded, born and bred member of CHRIST
                        APOSTOLIC CHURCH, I had thought that I was only to establish another branch of C.A.C church.
                        Sometimes in March 2001, I began a C.A.C. Rescue Camp Church, but shortly after, different
                        people began to bombard me with the revelations they had about me I also got some personal
                        directions. After a long but painful contemplation which was against my wish, I left the C.A.C
                        fold and changed the name of the church I had earlier formed to be RESCUE GLOBAL MINISTRIES in
                        2004. As to be expected, this action was not without criticism which resulted in me losing my
                        good standing with some people. But thank God that I objected. I immediately began the processes
                        of registering the RESCUE GOBAL MINISTRIES with the Corporate Affairs Commission (C.A.C) but due
                        to paucity of fund I couldn't precede with the registration for a period of five years. By 2009
                        when we went back to complete the registration process, I was told that the name was no longer
